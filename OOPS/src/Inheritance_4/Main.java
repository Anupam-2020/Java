package Inheritance_4;

public class Main {
    public static void main(String[] args) {

        // Inheritance allows code reusability by allowing one class to acquire fields and methods of another class.
        // Parent class is called superclass and child class is called subclass.
        // Multiple inheritance is not supported in Java.

        Dog dog = new Dog();
        dog.bark();
        dog.eat();
    }
}
